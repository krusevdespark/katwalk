package net.shiftinpower.objects;

public class Place {

	private int placeId;
	private String placeName;
	private String placeDescription;
	private int placeAddedByUser;
	private String placeAvatar;

	public int getPlaceId() {
		return placeId;
	}

	public void setPlaceId(int placeId) {
		this.placeId = placeId;
	}

	public String getPlaceName() {
		return placeName;
	}

	public void setPlaceName(String placeName) {
		this.placeName = placeName;
	}

	public String getPlaceDescription() {
		return placeDescription;
	}

	public void setPlaceDescription(String placeDescription) {
		this.placeDescription = placeDescription;
	}

	public int getPlaceAddedByUser() {
		return placeAddedByUser;
	}

	public void setPlaceAddedByUser(int placeAddedByUser) {
		this.placeAddedByUser = placeAddedByUser;
	}

	public String getPlaceAvatar() {
		return placeAvatar;
	}

	public void setPlaceAvatar(String placeAvatar) {
		this.placeAvatar = placeAvatar;
	}

}
