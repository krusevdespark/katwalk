package net.shiftinpower.objects;

public class Brand {

	private int brandId;
	private String brandName;
	private String brandLogo;
	private String brandDescription;
	private String brandWebsite;
	private int brandFirstAddedFrom;
	
	public int getBrandId() {
		return brandId;
	}
	public void setBrandId(int brandId) {
		this.brandId = brandId;
	}
	public String getBrandName() {
		return brandName;
	}
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	public String getBrandLogo() {
		return brandLogo;
	}
	public void setBrandLogo(String brandLogo) {
		this.brandLogo = brandLogo;
	}
	public String getBrandDescription() {
		return brandDescription;
	}
	public void setBrandDescription(String brandDescription) {
		this.brandDescription = brandDescription;
	}
	public String getBrandWebsite() {
		return brandWebsite;
	}
	public void setBrandWebsite(String brandWebsite) {
		this.brandWebsite = brandWebsite;
	}
	public int getBrandFirstAddedFrom() {
		return brandFirstAddedFrom;
	}
	public void setBrandFirstAddedFrom(int brandFirstAddedFrom) {
		this.brandFirstAddedFrom = brandFirstAddedFrom;
	}
	
	
}
